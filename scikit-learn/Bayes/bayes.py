#创建一个词集合                 
def createVocabList(dataSet):
    vocabSet = set([])  
    for document in dataSet:
        vocabSet = vocabSet | set(document) 
    return list(vocabSet)

#转化为数组
def setOfWords2Vec(vocabList, inputSet):
    returnVec = [0]*len(vocabList)
    for word in inputSet:
        if word in vocabList:
            returnVec[vocabList.index(word)] = 1
        else: print("the word: %s is not in my Vocabulary!" % word)
    return returnVec

#朴素贝叶斯分类器训练函数
def trainNB0(trainMatrix, trainCategory):
    numTrainDocs = len(trainMatrix)
    numWords = len(trainMatrix[0])
    pAbusive = sum(trainCategory)/float(numTrainDocs) #某一类别文档占总文档的比例
    p0Num = ones(numWords); p1Num = ones(numWords)
    p0Denom = 2.0; p1Denom = 2.0
    for i in range(numTrainDocs):
        if trainCategory[i] == 1:
            p1Num += trainMatrix[i]
            p1Denom += sum(trainMatrix[i])
        else:
            p0Num += trainMatrix[i]
            p0Denom += sum(trainMatrix[i])
    p1Vect = log(p1Num/p1Denom)                      #1类词条占所有与文档的概率
    p0Vect = log(p0Num/p0Denom)                      #0类词条占所有词条的概率
    #print(p1Vect, p0Vect)                      
    return p0Vect, p1Vect, pAbusive

#朴素贝叶斯分类函数
def classifyNB(vec2Classfy, p0Vec, p1Vec, pClass1):
    p1 = sum(vec2Classfy * p1Vec) + log(pClass1)
    p0 = sum(vec2Classfy * p0Vec) + log(1 - pClass1)
    #print(p1, p0)
    if p1 > p0:
        return 1
    else:
        return 0
